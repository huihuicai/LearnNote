public class SettingBean {

    private int mSettingId;
    private String mSettingName;
    private int mSettingStatus;

    public int getmSettingId() {
        return mSettingId;
    }

    public void setmSettingId(int mSettingId) {
        this.mSettingId = mSettingId;
    }

    public String getmSettingName() {
        return mSettingName;
    }

    public void setmSettingName(String mSettingName) {
        this.mSettingName = mSettingName;
    }

    public int getmSettingStatus() {
        return mSettingStatus;
    }

    public void setmSettingStatus(int mSettingStatus) {
        this.mSettingStatus = mSettingStatus;
    }
}
