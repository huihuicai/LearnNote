public class CourseBean {

    private int mCourseId;
    private String mCourseName;
    private int mCourseChapterNum;

    public int getmCourseId() {
        return mCourseId;
    }

    public void setmCourseId(int mCourseId) {
        this.mCourseId = mCourseId;
    }

    public String getmCourseName() {
        return mCourseName;
    }

    public void setmCourseName(String mCourseName) {
        this.mCourseName = mCourseName;
    }

    public int getmCourseChapterNum() {
        return mCourseChapterNum;
    }

    public void setmCourseChapterNum(int mCourseChapterNum) {
        this.mCourseChapterNum = mCourseChapterNum;
    }
}
